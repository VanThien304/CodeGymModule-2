package com.codegym;

public class Staff {
    private  String fullname,email;

    public void setFullname(String fullname){
        this.fullname = fullname;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getFullname(){
        return this.fullname;
    }
    public String getEmail(){
        return this.email;
    }

}
